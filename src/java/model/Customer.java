package model;

/**
 *
 * @author dmx
 */
public class Customer {
    private int id, userId;

    public Customer(int id, int userId) {
        this.id = id;
        this.userId = userId;
    }

    public Customer() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
    
}
